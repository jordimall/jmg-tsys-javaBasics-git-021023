package actividad3;

public class actividad3App {

	public static void main(String[] args) {
		int X = 5;
		int Y = 10;
		double N = 3.14;
		double M = 9.54;
		
		double resultado = 0;
		
		System.out.println("El valor de la variable X es: " + X);
		System.out.println("El valor de la variable Y es: " + Y);
		System.out.println("El valor de la variable N es: " + N);
		System.out.println("El valor de la variable M es: " + M);
		
		resultado = X + Y;
		System.out.println("El resultado de la suma de las variables X e Y es: " + resultado);
		
		resultado = X - Y;
		System.out.println("El resultado de la resta de las variables X e Y es: " + resultado);
		
		resultado = X * Y;
		System.out.println("El resultado de la multiplicación de las variables X e Y es: " + resultado);
		
		resultado = X / Y;
		System.out.println("El resultado de la división de las variables X e Y es: " + resultado);
		
		resultado = X % Y;
		System.out.println("La resta de la división de las variables X e Y es: " + resultado);
		
		resultado = N + M;
		System.out.println("El resultado de la suma de las variables N e M es: " + resultado);
		
		resultado = N - M;
		System.out.println("El resultado de la resta de las variables N e M es: " + resultado);
		
		resultado = N * M;
		System.out.println("El resultado de la multiplicación de las variables N e M es: " + resultado);
		
		resultado = N / M;
		System.out.println("El resultado de la división de las variables N e M es: " + resultado);
		
		resultado = N % M;
		System.out.println("La resta de la división de las variables N e M es: " + resultado);
		
		resultado = X * 2;
		System.out.println("El doble de la variable X es: " + resultado);
		
		resultado = Y * 2;
		System.out.println("El doble de la variable Y es: " + resultado);
		
		resultado = N * 2;
		System.out.println("El doble de la variable N es: " + resultado);
		
		resultado = M * 2;
		System.out.println("El doble de la variable M es: " + resultado);
		
		resultado = X + Y + N + M;
		System.out.println("La suma de todas las variables es: " + resultado);
		
		resultado = X * Y * N * M;
		System.out.println("La multiplicación de todas las variables es: " + resultado);

	}

}
